fn main() {
    // 참고: 두 번째 줄부터는 맨 왼쪽부터 시작해야 합니다.
    // println!이 시작되는 위치에 맞춰 들여쓰기를 하면 공백이 추가됩니다.
    println!("Inside quotes
   you can write over
   many lines
   and it will print just fine.");
    println!(
        "If you forget to write
    on the left side, the spaces
    will be added when you print."
    );
}
